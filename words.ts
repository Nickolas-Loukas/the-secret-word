export function getRandomWord(language: 'greek' | 'english' = 'greek'): string {
  const words = language === 'english' ? ENGLISH_WORDS : GREEK_WORDS;
  return words[Math.floor(Math.random() * words.length)];
}

export function getRandomWords(count: number, language: 'greek' | 'english' = 'greek'): string[] {
  const words = language === 'english' ? ENGLISH_WORDS : GREEK_WORDS;
  const shuffled = [...words].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}
